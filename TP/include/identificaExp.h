#ifndef IDENTIFICA_EXP_H
#define IDENTIFICA_EXP_H

/**
 * @brief Identifica se uma expressão é Infixa, retornando 1 (true) caso verdadeiro.
 * 
 * @param exp - expressão passada como parâmetro
 * @return * int 
 */
int identificaExpInfixa(char *exp);

/**
 * @brief Identifica se uma expressão é Posfixa, retornando 1 (true) caso verdadeiro.
 * 
 * @param exp - expressão passada como parâmetro
 * @return * int 
 */
int identificaExpPosfixa(char *exp);

#endif